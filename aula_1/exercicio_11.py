readA = float(input('Digite um numero: '))
readB = float(input('Digite outro numero: '))
readC = int(input('Digite um numero inteiro: '))

print('O produto do dobro do primeiro com metade do segundo: %.1f' % ((readA * readA) * (readB / 2)))
print('A soma do triplo do primeiro com o terceiro: %.1f' % ((readA * 3) + readC))
print('O terceiro elevado ao cubo: %.1f' % (readC ** 4))